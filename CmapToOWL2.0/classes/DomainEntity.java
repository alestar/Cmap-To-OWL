
package classes;
/**
 * Clase entidad que permite manipular los dominios del MC
 * */
public class DomainEntity {

	private String domain;
	
	public DomainEntity(String domain) {
		this.domain = domain;
	}

	public String getDomain() {
		return domain;
	}

	public void setDomain(String domain) {
		this.domain = domain;
	}
	
}
